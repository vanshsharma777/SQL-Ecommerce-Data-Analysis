-- BASIC SQL ANALYSIS QUERIES 

SELECT COUNT(*) AS TOTAL_PRODUCTS FROM PRODUCT;
SELECT COUNT(*) AS TOTAL_ORDERS FROM ORDERS;
SELECT SUM(AMOUNT) AS REVENUE FROM PAYMENTS;
SELECT COUNT(ORDER_STATUS) AS DELIVERD_OUDER FROM ORDERS WHERE ORDER_STATUS = 'DELIVERED';
SELECT COUNT(ORDER_STATUS) AS DELIVERD_OUDER FROM ORDERS WHERE ORDER_STATUS = 'PENDING';
SELECT SUM(AMOUNT) AS UPI_AMOUNT FROM PAYMENTS WHERE PAYMENT_METHOD = 'UPI';
SELECT SUM(AMOUNT) AS CARD_AMOUNT FROM PAYMENTS WHERE PAYMENT_METHOD = 'CARD';
SELECT SUM(AMOUNT) AS CARD_AMOUNT FROM PAYMENTS WHERE PAYMENT_METHOD = 'CASH';
SELECT PAYMENT_METHOD, SUM(AMOUNT) AS REVENUE FROM PAYMENTS GROUP BY PAYMENT_METHOD ;
SELECT PRODUCT_NAME,PRICE FROM PRODUCT ORDER BY PRICE DESC;
