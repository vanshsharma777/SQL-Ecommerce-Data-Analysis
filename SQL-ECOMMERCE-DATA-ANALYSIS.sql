-- PORTFOLIO LEVEL QUERIES

SELECT PRODUCT.PRODUCT_NAME , SUM(ORDERS_ITEM.QUANTITY) AS TOTAL_SOLD FROM PRODUCT 
INNER JOIN ORDERS_ITEM ON PRODUCT.PRODUCT_ID = ORDERS_ITEM.PRODUCT_ID 
GROUP BY PRODUCT.PRODUCT_NAME ORDER BY TOTAL_SOLD DESC;

SELECT PAYMENT_MATHOD , SUM(TOTAL_AMOUNT) AS REVENUE FROM ORDERS GROUP BY PAYMENT_MATHOD;

SELECT ORDER_STATUS ,COUNT(*) AS TOTAL_ORDER FROM ORDERS GROUP BY ORDER_STATUS;

SELECT CUSTOMER_NAME , SUM(ORDERS.TOTAL_AMOUNT) AS TOTAL_SPENT FROM CUSTOMER 
INNER JOIN ORDERS ON CUSTOMER.CUSTOMER_ID = ORDERS.CUSTOMER_ID 
GROUP BY CUSTOMER_NAME ORDER BY TOTAL_SPENT DESC;

